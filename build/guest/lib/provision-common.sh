# Suricata 8.x ./configure flags. Rust is mandatory in 8.x (no --enable-rust) and provides
# JA3/JA4 natively; jansson is auto-detected (libjansson-dev present); --enable-lua was dropped.
# So only the transport flags are passed; NFQUEUE is the IPS-critical one.
pc_suricata_configure_args() {
  cat <<'EOF'
--prefix=/usr --sysconfdir=/etc --localstatedir=/var
--enable-nfqueue
--enable-af-packet
EOF
}

# systemd unit hardening drop-in (stdout). $1 = unit name (for the comment header).
# NB: PrivateDevices and MemoryDenyWriteExecute are intentionally OMITTED — they break
# Suricata (raw /dev/net/tun + nfqueue, Lua-JIT) and risk eBPF daemons (OpenSnitch). The
# remaining directives are broadly daemon-safe; per-service device/JIT needs are tuned in Plan C.
pc_systemd_hardening_dropin() {
  local unit="${1:?unit name}"
  cat <<EOF
# hardening drop-in for ${unit}.service (generated by provision.sh)
[Service]
NoNewPrivileges=yes
ProtectSystem=strict
ProtectHome=yes
PrivateTmp=yes
ProtectControlGroups=yes
ProtectKernelModules=yes
ProtectKernelTunables=yes
RestrictSUIDSGID=yes
RestrictRealtime=yes
LockPersonality=yes
EOF
}

