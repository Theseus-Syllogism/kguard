# The script kguard-whonix-prep.service runs. Formats the blank state disk on first boot (never
# reformatting one that already carries a filesystem — that would destroy persisted nested
# state), mounts it at WHONIX_HOME, and installs the nested LUKS key from the RO config share.
# Fail-closed: if the host didn't deliver a key, refuse to proceed (whonix start needs a 0600
# keyfile anyway). Env vars are overridable for tests; defaults match kguard's qemu wiring.
pc_whonix_prep_script() {
  cat <<'EOF'
#!/usr/bin/env bash
set -euo pipefail
STATE_DEV="${KGUARD_STATE_DEV:-/dev/vdb}"      # second virtio-blk disk (whonix-state.qcow2)
STATE_DIR="${WHONIX_HOME:-/var/lib/whonix-runner}"
CFG_MNT="${KGUARD_CONFIG_MNT:-/run/kguard-config}"
KEY_SRC="$CFG_MNT/whonix.luks.key"
KEY_DST="$STATE_DIR/.luks.key"

# 1. nested-state disk: format the blank qcow2 on first boot, then mount it. A disk that already
#    carries a filesystem is left intact (it holds the encrypted nested overlays + key).
if [ -b "$STATE_DEV" ]; then
  blkid "$STATE_DEV" >/dev/null 2>&1 || mkfs.ext4 -q "$STATE_DEV"
  mkdir -p "$STATE_DIR"
  mountpoint -q "$STATE_DIR" || mount "$STATE_DEV" "$STATE_DIR"
else
  mkdir -p "$STATE_DIR"   # no block device (test / no-state-disk fallback)
fi
chmod 700 "$STATE_DIR"

# 2. install the nested-Whonix LUKS key delivered by `kguard start` via the RO config share.
#    Fail closed if it's absent: better to not boot nested Whonix than to boot it unencrypted.
if [ ! -r "$KEY_SRC" ]; then
  echo "kguard-whonix-prep: nested key $KEY_SRC missing — refusing to start nested Whonix" >&2
  exit 1
fi
install -m 600 "$KEY_SRC" "$KEY_DST"
EOF
}

# qemu external-NIC fragment for the nested Gateway: a TAP attached to the pre-created L1
# bridge (kg-gw0 on br-whonix), replacing whonix-runner's default SLIRP user netdev so the
# Gateway's Tor egress traverses L1's kernel netfilter (where Suricata NFQUEUE sits).
pc_whonix_tap_netdev() {
  printf '%s\n' \
    '-netdev tap,id=ext,ifname=kg-gw0,script=no,downscript=no' \
    '-device virtio-net-pci,netdev=ext,mac=52:54:00:a1:b2:01'
}

# Emit a systemctl enable/disable plan for the optional detectors based on /etc/kguard.conf
# (the 9p-mounted host-local config). Missing/0 => disabled (secure default). $1 = config path.
pc_feature_activation() {
  local cfg="${1:-/etc/kguard.conf}"
  # shellcheck disable=SC2034  # DEBSUMS: reserved config key, not yet wired to a verb
  local ZEEK=0 DNSCRYPT=0 CROWDSEC=0 FALCO=0 DEBSUMS=0 WAZUH_ENDPOINT=""
  # shellcheck source=/dev/null
  [[ -r "$cfg" ]] && source "$cfg"
  _kg_verb() { [[ "${1:-0}" == "1" ]] && echo "enable $2" || echo "disable $2"; }
  _kg_verb "$ZEEK" zeek
  _kg_verb "$DNSCRYPT" dnscrypt-proxy
  _kg_verb "$CROWDSEC" crowdsec
  _kg_verb "$FALCO" falco
  [[ -n "$WAZUH_ENDPOINT" ]] && echo "configure wazuh $WAZUH_ENDPOINT" || echo "skip wazuh"
}

# Runnable cowrie.cfg overlay (cowrie 3.0 loads its bundled cowrie.cfg.dist as defaults, then
# overlays ./etc/cowrie.cfg relative to the working dir). Enables the ssh+telnet honeypot on
# 2222/2223 and points state/logs at absolute paths under the cowrie home, so it works under the
# unit's ProtectSystem=strict + ReadWritePaths. $1 = cowrie home (default /opt/cowrie).
pc_cowrie_cfg() {
  local home="${1:-/opt/cowrie}"
  cat <<EOF
# kguard cowrie honeypot config (overlays the bundled cowrie.cfg.dist).
[honeypot]
hostname = svr04
log_path = ${home}/var/log/cowrie
state_path = ${home}/var/lib/cowrie

[ssh]
enabled = true
listen_endpoints = tcp:2222:interface=0.0.0.0

[telnet]
enabled = true
listen_endpoints = tcp:2223:interface=0.0.0.0

[output_jsonlog]
enabled = true
logfile = ${home}/var/log/cowrie/cowrie.json
EOF
}

# systemd unit for the pip-installed Cowrie 3.0 honeypot. cowrie 3.0 runs in the foreground when
# COWRIE_STDOUT=yes (twistd -n -l -) — the old `cowrie start -n` is an invalid flag in 3.0 and the
# reason the service stayed 'activating'. WorkingDirectory must hold etc/cowrie.cfg (cowrie's init
# marker); PATH must include the venv so `cowrie` can exec `twistd`; runs as the unprivileged
# cowrie user (cowrie refuses to run as root). ProtectSystem=strict needs the var/ ReadWritePath.
pc_cowrie_unit() {
  cat <<'EOF'
[Unit]
Description=Cowrie SSH/Telnet honeypot
After=network-online.target
Wants=network-online.target
[Service]
Type=simple
User=cowrie
WorkingDirectory=/opt/cowrie
Environment=PATH=/opt/cowrie/venv/bin:/usr/bin:/bin
Environment=COWRIE_STDOUT=yes
ExecStart=/opt/cowrie/venv/bin/cowrie start
Restart=on-failure
RestartSec=5
ProtectSystem=strict
ReadWritePaths=/opt/cowrie/var
[Install]
WantedBy=multi-user.target
EOF
}
