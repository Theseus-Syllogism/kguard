# Fail-closed leak-guard nftables ruleset for L1. Nested Whonix egress enters on br-whonix;
# established/related is fast-pathed, new traffic is sent to NFQUEUE 0 (Suricata IPS), and the
# forward policy is DROP so anything Suricata doesn't explicitly accept cannot leave. IPv6 is
# dropped outright (anti-leak backstop below Tor). $1 = uplink iface (default: $KGUARD_UPLINK
# or eth0). Override if the baked image uses predictable NIC names (net.ifnames=1).
pc_nft_ruleset() {
  local uplink="${1:-${KGUARD_UPLINK:-eth0}}"
  cat <<EOF
#!/usr/sbin/nft -f
flush ruleset
table inet kguard {
  chain forward {
    type filter hook forward priority 0; policy drop;
    meta nfproto ipv6 drop
    ct state established,related accept
    iifname "br-whonix" oifname "$uplink" queue num 0
  }
  chain input {
    type filter hook input priority 0; policy drop;
    meta nfproto ipv6 drop
    iif "lo" accept
    ct state established,related accept
    iifname "br-whonix" ct state new accept
  }
}
table ip kguard_nat {
  chain postrouting {
    type nat hook postrouting priority 100; policy accept;
    oifname "$uplink" masquerade
  }
}
EOF
}

# Suricata inline IPS unit: NFQUEUE 0, fail-closed (no --queue-bypass: if Suricata dies, the
# nft forward policy=drop means traffic stops rather than leaking). Minimal capabilities.
pc_suricata_ips_unit() {
  cat <<'EOF'
[Unit]
Description=Suricata inline IPS (NFQUEUE 0, fail-closed)
After=kguard-net.service
Wants=kguard-net.service
[Service]
Type=simple
ExecStart=/usr/bin/suricata -q 0 --pidfile /run/suricata-ips.pid -c /etc/suricata/suricata.yaml
Restart=on-failure
RestartSec=2
AmbientCapabilities=CAP_NET_ADMIN CAP_NET_RAW
CapabilityBoundingSet=CAP_NET_ADMIN CAP_NET_RAW
NoNewPrivileges=yes
ProtectSystem=strict
ProtectHome=yes
ReadWritePaths=/var/log/suricata /var/lib/suricata /run
[Install]
WantedBy=multi-user.target
EOF
}

# Known-good Suricata config for the inline NFQUEUE IPS. The stock suricata.yaml shipped by
# 'make install-full' is tuned for IDS/af-packet, so the -q 0 unit never reaches stable active
# (Gap #2, RUNTIME-FINDINGS-2026-06-06). This is a minimal, self-contained inline config:
#   * nfq fail-open:no  -> fail-closed (matches the no --queue-bypass unit + nft policy drop)
#   * log/rule dirs aligned with the unit's ReadWritePaths (/var/log/suricata, /var/lib/suricata)
#   * rules are populated at runtime (suricata-update); a clean boot with zero rules still cannot
#     leak because the nft forward policy is DROP. $1 = rule-path, $2 = log-dir (overridable for tests).
pc_suricata_yaml() {
  local rule_path="${1:-/var/lib/suricata/rules}"
  local log_dir="${2:-/var/log/suricata}"
  cat <<EOF
%YAML 1.1
---
# kguard inline-IPS config (NFQUEUE 0, fail-closed) — generated by provision.sh.
# HOME_NET is the nested-Whonix L1 segment (br-whonix 10.0.2.0/24). The full standard rule-var
# set is defined so a loaded ruleset (ET Open via suricata-update at runtime) parses — a minimal
# vars block silently breaks signature loading the moment real rules are present.
vars:
  address-groups:
    HOME_NET: "[10.0.2.0/24]"
    EXTERNAL_NET: "!\$HOME_NET"
    HTTP_SERVERS: "\$HOME_NET"
    SMTP_SERVERS: "\$HOME_NET"
    SQL_SERVERS: "\$HOME_NET"
    DNS_SERVERS: "\$HOME_NET"
    TELNET_SERVERS: "\$HOME_NET"
    AIM_SERVERS: "\$EXTERNAL_NET"
    DC_SERVERS: "\$HOME_NET"
    DNP3_SERVER: "\$HOME_NET"
    DNP3_CLIENT: "\$HOME_NET"
    MODBUS_CLIENT: "\$HOME_NET"
    MODBUS_SERVER: "\$HOME_NET"
    ENIP_CLIENT: "\$HOME_NET"
    ENIP_SERVER: "\$HOME_NET"
  port-groups:
    HTTP_PORTS: "80"
    SHELLCODE_PORTS: "!80"
    ORACLE_PORTS: 1521
    SSH_PORTS: 22
    DNP3_PORTS: 20000
    MODBUS_PORTS: 502
    FILE_DATA_PORTS: "[\$HTTP_PORTS,110,143]"
    FTP_PORTS: 21
    GENEVE_PORTS: 6081
    VXLAN_PORTS: 4789
    TEREDO_PORTS: 3544
    SIP_PORTS: "[5060, 5061]"

default-log-dir: ${log_dir}
default-rule-path: ${rule_path}
rule-files:
  - suricata.rules

# Inline IPS via NFQUEUE 0. fail-open:no is the fail-closed contract: if Suricata can't issue a
# verdict the packet is NOT released — the nft forward policy=drop is the backstop either way.
nfq:
  mode: accept
  fail-open: no
  batchcount: 20

outputs:
  - eve-log:
      enabled: yes
      filetype: regular
      filename: eve.json
      types:
        - alert
        - drop
  - stats:
      enabled: no

logging:
  default-log-level: notice
  outputs:
    - console:
        enabled: yes

app-layer:
  protocols:
    tls:
      enabled: yes
    http:
      enabled: yes
    dns:
      enabled: yes
EOF
}

# Oneshot that builds the L1 bridge + Gateway TAP and loads the nft ruleset, before any VM.
pc_kguard_net_unit() {
  cat <<'EOF'
[Unit]
Description=kguard L1 network (br-whonix + TAP + nftables leak-guard)
After=network-pre.target
Before=whonix-autostart.service
[Service]
Type=oneshot
RemainAfterExit=yes
ExecStart=/usr/local/sbin/kguard-net-setup.sh
[Install]
WantedBy=multi-user.target
EOF
}

# The script kguard-net.service runs: bridge, TAP, ip_forward, and load the nft ruleset.
pc_kguard_net_setup_script() {
  cat <<'EOF'
#!/usr/bin/env bash
set -euo pipefail
# Bridge the nested Gateway's external NIC into L1's kernel path so Suricata (NFQUEUE) sees it.
ip link show br-whonix >/dev/null 2>&1 || ip link add name br-whonix type bridge
ip link set br-whonix up
ip link show kg-gw0 >/dev/null 2>&1 || ip tuntap add dev kg-gw0 mode tap
ip link set kg-gw0 master br-whonix
ip link set kg-gw0 up
ip addr show dev br-whonix | grep -q '10.0.2.2/24' || ip addr add 10.0.2.2/24 dev br-whonix
sysctl -w net.ipv4.ip_forward=1
nft -f /etc/nftables.d/kguard.conf
EOF
}

# Autostart the nested Whonix stack (via the vendored, TAP-adapted whonix-runner) after the
# network + IPS are up. Runs as root; the launcher boots Gateway then Workstation headlessly.
pc_whonix_autostart_unit() {
  cat <<'EOF'
[Unit]
Description=kguard nested Whonix (Gateway + Workstation)
After=kguard-net.service suricata-ips.service kguard-whonix-prep.service
Wants=kguard-net.service suricata-ips.service kguard-whonix-prep.service
RequiresMountsFor=/var/lib/whonix-images /var/lib/whonix-runner
[Service]
Type=oneshot
RemainAfterExit=yes
ExecStart=/usr/local/bin/whonix start
ExecStop=/usr/local/bin/whonix stop
[Install]
WantedBy=multi-user.target
EOF
}

