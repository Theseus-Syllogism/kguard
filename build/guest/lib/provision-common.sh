# Boot oneshot: prepare the nested-state disk and install the nested-Whonix LUKS key. Ordered
# after the kguard-config mount and before whonix-autostart so `whonix start` finds a 0600 key.
pc_whonix_prep_unit() {
  cat <<'EOF'
